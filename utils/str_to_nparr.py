import numpy as np
def str2nparr(sss):
    arr=[]
    for ss in sss.strip().split('\n'):
        arri=[]
        if ss.strip():
            for s in ss.strip().split():
                arri.append(float(s))
            arr.append(arri)
    return np.array(arr)

